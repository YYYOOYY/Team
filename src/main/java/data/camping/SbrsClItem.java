package data.camping;

public class SbrsClItem {
	String name;
	String resource;

	public SbrsClItem() {
		super();
	}

	public SbrsClItem(String name, String resource) {
		super();
		this.name = name;
		this.resource = resource;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getResource() {
		return resource;
	}

	public void setResource(String resource) {
		this.resource = resource;
	}

}
