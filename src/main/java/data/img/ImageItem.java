package data.img;

public class ImageItem {
	// Key값으로 사용할 Id값
	String contentId;
	
	// 이미지 시리얼넘버
	String serialnum;
	
	// 이미지 url
	String imageUrl;

	public String getContentId() {
		return contentId;
	}

	public String getSerialnum() {
		return serialnum;
	}

	public String getImageUrl() {
		return imageUrl;
	}
}
