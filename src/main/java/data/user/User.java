package data.user;

public class User {
	// 유저 아이디
	String id;
	// 유저 비밀번호
	String pass;
	// 유저 닉네임
	String nick;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}

	
}
